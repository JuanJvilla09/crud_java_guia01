package org.example.modelo;

public class Propietario {
    private String idprop;
    private String nombre;
    private String telefono;

    public Propietario() {
    }

    public Propietario(String idprop, String nombre, String telefono) {
        this.idprop = idprop;
        this.nombre = nombre;
        this.telefono = telefono;
    }
}
